package beans;

public class AddressActorBean {

    private int address_actor_id;
    private String floor;
    private String street;
    private String landmark;
    private String sector;
    private String city;
    private String state;
    private String pin_code;
    private String actor_unique_id;
    private String creation_date;
    private String status;

    public int getAddress_actor_id() {
        return address_actor_id;
    }

    public void setAddress_actor_id(int address_actor_id) {
        this.address_actor_id = address_actor_id;
    }

    public String getFloor() {
        return floor;
    }

    public void setFloor(String floor) {
        this.floor = floor;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getLandmark() {
        return landmark;
    }

    public void setLandmark(String landmark) {
        this.landmark = landmark;
    }

    public String getSector() {
        return sector;
    }

    public void setSector(String sector) {
        this.sector = sector;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getPin_code() {
        return pin_code;
    }

    public void setPin_code(String pin_code) {
        this.pin_code = pin_code;
    }

    public String getActor_unique_id() {
        return actor_unique_id;
    }

    public void setActor_unique_id(String actor_unique_id) {
        this.actor_unique_id = actor_unique_id;
    }

    public String getCreation_date() {
        return creation_date;
    }

    public void setCreation_date(String creation_date) {
        this.creation_date = creation_date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
